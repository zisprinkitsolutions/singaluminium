     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $currency = Currency::where('id' ,1)->first();
        $currency->country_name =$request->country_name;
        $currency->currency_nane =$request->currency_nane;
        $currency->symbole =$request->symbol;
        $currency->licence_name =$request->licence_name;
        $currency->vat_name =$request->vat_name;
        $currency->save();
        return redirect()->back()->with('success','Added Successfully');

    }

